const KEY = (a: string, b: string) => `${a}->${b}`;
const EDGE_MAP: Record<string, ChainEdge> = {};
for (const e of CHAIN_EDGES) EDGE_MAP[KEY(e.from, e.to)] = e;

// 取两点间的边(方向无关),没有返回 null。
export function chainEdge(a: string, b: string): ChainEdge | null {
  return EDGE_MAP[KEY(a, b)] ?? EDGE_MAP[KEY(b, a)] ?? null;
}

export interface ChainNeighbor {
  code: string;
  strength: ChainStrength;
  basis: string;
  dir: "up" | "down"; // up = 该邻居是本股的上游(供货给本股);down = 下游(采购本股)
}

const RANK: Record<ChainStrength, number> = { 强: 3, 中: 2, 弱: 1 };
const NEIGHBORS: Record<string, ChainNeighbor[]> = {};
function add(code: string, n: ChainNeighbor) {
  const arr = (NEIGHBORS[code] = NEIGHBORS[code] ?? []);
  const ex = arr.find((x) => x.code === n.code);
  if (!ex) arr.push(n);
  else if (RANK[n.strength] > RANK[ex.strength]) Object.assign(ex, n);
}
for (const e of CHAIN_EDGES) {
  // 对 from 而言,to 是它的下游;对 to 而言,from 是它的上游
  add(e.from, { code: e.to, strength: e.strength, basis: e.basis, dir: "down" });
  add(e.to, { code: e.from, strength: e.strength, basis: e.basis, dir: "up" });
}
for (const code in NEIGHBORS)
  NEIGHBORS[code].sort((a, b) => RANK[b.strength] - RANK[a.strength]);

// 本股在池内的全部真实关联邻居(已按强度降序、去重)。
export function chainNeighbors(code: string): ChainNeighbor[] {
  return NEIGHBORS[code] ?? [];
}
